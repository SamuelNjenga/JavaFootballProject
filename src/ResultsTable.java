    public void fetch_home_team_details(String home_team_id,String home_team_goals,String away_team_id,String away_team_goals){
        Connection con = null;
        ResultSet rs = null;
        PreparedStatement ps = null;
        
        try{
                Class.forName("com.mysql.jdbc.Driver");
                String url = "jdbc:mysql://localhost:3306/kabumbu";
                String pass = "";
                String uname = "root";
                con = DriverManager.getConnection(url,uname,pass);
                String sql = "select * from league where teamIdNumber = ?";
                ps = con.prepareStatement(sql);
                ps.setString(1,home_team_id); 
                rs = ps.executeQuery();
                
                if(rs.next()){
                    String home_name        = rs.getString("teamName");
                    int home_goals_for      = rs.getInt("goalsFor");
                    int home_goals_against  = rs.getInt("goalsAgainst");
                    int home_points         = rs.getInt("points");

                    
                    global_home_goal_for = home_goals_for;
                    global_home_goal_against = home_goals_against;
                    global_home_points = home_points;
                    home_team_name = home_name;
                
                    fetch_away_team_details(home_team_id,home_team_goals,away_team_id,away_team_goals);
                
                }else
                {
                    JOptionPane.showMessageDialog(null, "Home Team Inserted doesn't Exist");
                }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    }
