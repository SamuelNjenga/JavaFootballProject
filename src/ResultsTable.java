    public void update_match_details(String home_id, String home_goals, String away_id, String away_goals){        
        int home_goals_x = Integer.parseInt(home_goals);
        int away_goals_x = Integer.parseInt(away_goals);
        
        global_home_goal_for        = global_home_goal_for + home_goals_x;
        global_home_goal_against    = global_home_goal_against + away_goals_x;
        
        global_away_goal_for        = global_away_goal_for + away_goals_x;
        global_away_goal_against    = global_away_goal_against + home_goals_x;
        
        
     
        
        if(jRadioButton3.isSelected()){
           
            global_home_points = global_home_points + 0;
            global_away_points = global_away_points + 2;
            
        }
        else if(jRadioButton2.isSelected())
        {
            
            global_home_points = global_home_points + 2;
            global_away_points = global_away_points + 0;
        }
        else
        {
            
        
        if(home_goals_x>away_goals_x){
            //Home Win
            global_home_points = global_home_points + 3;
            global_away_points = global_away_points + 0;
            
        }else if(home_goals_x == away_goals_x){
            //Draw
            global_home_points = global_home_points + 1;
            global_away_points = global_away_points + 1;
            
        }else{
            //Away win
            global_home_points = global_home_points + 0;
            global_away_points = global_away_points + 3;
        }
        }
        update_home_team_details(home_id,away_id);
        
    
    }
